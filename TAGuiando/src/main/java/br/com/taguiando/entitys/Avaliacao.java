package br.com.taguiando.entitys;

public class Avaliacao {

	public int codigo;
	public int codigo_pro;
	public int codigo_tag;
	public int matricula_alu;
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public int getCodigo_pro() {
		return codigo_pro;
	}
	public void setCodigo_pro(int codigo_pro) {
		this.codigo_pro = codigo_pro;
	}
	public int getCodigo_tag() {
		return codigo_tag;
	}
	public void setCodigo_tag(int codigo_tag) {
		this.codigo_tag = codigo_tag;
	}
	public int getMatricula_alu() {
		return matricula_alu;
	}
	public void setMatricula_alu(int matricula_alu) {
		this.matricula_alu = matricula_alu;
	}
	
	
}
